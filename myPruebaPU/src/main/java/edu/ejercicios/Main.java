// Hibernate es un framework de ORM (Object-Relational Mapping) para Java que permite
// interactuar con bases de datos relacionales utilizando objetos y abstracciones de la capa
// de presentación de la aplicación, en lugar de trabajar directamente con las tablas y consultas
// de la base de datos.
// En otras palabras, Hibernate se encarga de traducir las operaciones que se realizan en los
// objetos de la aplicación en operaciones que se pueden realizar en la base de datos, y
// viceversa. Esto permite que los desarrolladores de aplicaciones se enfóquen en el desarrollo
// de la lógica de negocio y la capa de presentación de la aplicación, sin tener que preocuparse
// por la implementación detallada de la capa de acceso a datos.

// Un framework es una estructura de software que proporciona una serie de herramientas
// y patrones para desarrollar aplicaciones de manera más rápida y eficiente. En otras palabras,
// un framework es una plataforma que ayuda a los desarrolladores a crear aplicaciones de manera
// más sencilla y estándar.
// Los frameworks suelen proporcionar una serie de características, como:
// Arquitectura modular, herramientas de desarrollo, patrones de diseño, interfaz de usuario, seguridad.


// El mapeo se refiere a la relación entre las clases de objetos de la aplicación y las tablas
// de la base de datos. El objetivo del mapeo es permitir que los objetos de la aplicación
// se mapeen a las tablas de la base de datos y viceversa.
// En otras palabras, el mapeo se encarga de establecer la relación entre los objetos de la
// aplicación y las tablas de la base de datos, y de traducir las operaciones que se realizan
// en los objetos de la aplicación en operaciones que se pueden realizar en la base de datos.

// El mapeo es un proceso importante en Hibernate, ya que permite que los objetos de la
// aplicación se mapeen a las tablas de la base de datos de manera eficiente y segura.
// De esta manera, Hibernate puede realizar operaciones de consulta y actualización
// en la base de datos de manera transparente y fácil de usar.
// El mapeo se realiza mediante el uso de annotations en Hibernate, como @Entity,
// @Table, @Id, @Column, @ManyToOne, @OneToMany, entre otras.
// Por ejemplo, la annotation @Entity se utiliza para definir una clase como una tabla
// en la base de datos, mientras que la annotation @Table se utiliza para especificar
// el nombre de la tabla en la base de datos que se corresponde con la clase.
// La annotation @Id se utiliza para definir la columna principal de la tabla, que se
// utiliza para identificar cada registro en la tabla.